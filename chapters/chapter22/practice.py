"""가중치 행렬과 순전파 기초"""
# 설명: annotations를(을) 최신 파이썬 문법(annotations 등)을 이전 버전에서도 쓸 수 있게 해줘요.
from __future__ import annotations

# 설명: 수치 계산(배열·행렬·통계)을 위한 NumPy 라이브러리를 불러와요.
import numpy as np


# 설명: 'run' 함수를 정의해요.
def run() -> dict:
    # 설명: 파이썬 리스트·시퀀스를 NumPy 배열로 변환해요.
    X = np.array(
        # 설명: 이 코드를 실행해요.
        [
            # 설명: 이 코드를 실행해요.
            [0.5, 1.0, 0.2],
            # 설명: 이 코드를 실행해요.
            [1.5, 0.3, 0.7],
            # 설명: 이 코드를 실행해요.
            [0.1, 0.4, 1.2],
        # 설명: 이 코드를 실행해요.
        ]
    # 설명: 이 코드를 실행해요.
    )
    # 설명: 파이썬 리스트·시퀀스를 NumPy 배열로 변환해요.
    W = np.array(
        # 설명: 이 코드를 실행해요.
        [
            # 설명: 이 코드를 실행해요.
            [0.2, -0.1],
            # 설명: 이 코드를 실행해요.
            [0.4, 0.3],
            # 설명: 이 코드를 실행해요.
            [-0.5, 0.2],
        # 설명: 이 코드를 실행해요.
        ]
    # 설명: 이 코드를 실행해요.
    )
    # 설명: 파이썬 리스트·시퀀스를 NumPy 배열로 변환해요.
    b = np.array([[0.1, -0.2]])

    # 설명: z-점수(표준 정규 점수)를 계산해요 — (값 - 평균) / 표준편차.
    z = X @ W + b

    # 설명: '{'을(를) 함수 호출 측에 반환해요.
    return {
        # 설명: 이 코드를 실행해요.
        "chapter": "chapter22",
        # 설명: 이 코드를 실행해요.
        "topic": "가중치 행렬과 순전파",
        # 설명: 값을 파이썬 리스트로 변환해요.
        "input_shape": list(X.shape),
        # 설명: 값을 파이썬 리스트로 변환해요.
        "weight_shape": list(W.shape),
        # 설명: 값을 파이썬 리스트로 변환해요.
        "bias_shape": list(b.shape),
        # 설명: NumPy 배열을 파이썬 리스트로 변환해요.
        "linear_output": np.round(z, 4).tolist(),
    # 설명: 이 코드를 실행해요.
    }


# 설명: 이 파일을 직접 실행했을 때만 아래 코드를 수행해요.
if __name__ == "__main__":
    # 설명: 값을 정수형으로 변환해요.
    print(run())

# 설명: annotations를(을) 최신 파이썬 문법(annotations 등)을 이전 버전에서도 쓸 수 있게 해줘요.
from __future__ import annotations

# 설명: 수치 계산(배열·행렬·통계)을 위한 NumPy 라이브러리를 불러와요.
import numpy as np


# 설명: 10분 요약 학습 내용을 상수 문자열로 정의해요.
LESSON_10MIN = "뉴런은 입력에 가중치를 곱해 더한 뒤 출력값을 만든다."
# 설명: 30분 실습 목표를 상수 문자열로 정의해요.
PRACTICE_30MIN = "numpy로 1층 순전파를 계산하고 출력 형태를 확인한다."


# 설명: 'run' 함수를 정의해요.
def run() -> dict:
    # 3개 샘플, 각 샘플은 4개 특성
    # 설명: 파이썬 리스트·시퀀스를 NumPy 배열로 변환해요.
    X = np.array(
        # 설명: 이 코드를 실행해요.
        [
            # 설명: 이 코드를 실행해요.
            [0.2, 0.7, 1.0, -0.3],
            # 설명: 이 코드를 실행해요.
            [1.2, -0.4, 0.3, 0.8],
            # 설명: 이 코드를 실행해요.
            [0.0, 0.5, -0.6, 1.1],
        # 설명: 이 코드를 실행해요.
        ],
        # 설명: 'dtype' 변수에 값을 계산해서 저장해요.
        dtype=float,
    # 설명: 이 코드를 실행해요.
    )

    # 1층(입력 4 -> 출력 3)
    # 설명: 파이썬 리스트·시퀀스를 NumPy 배열로 변환해요.
    W = np.array(
        # 설명: 이 코드를 실행해요.
        [
            # 설명: 이 코드를 실행해요.
            [0.4, -0.2, 0.1],
            # 설명: 이 코드를 실행해요.
            [0.7, 0.3, -0.5],
            # 설명: 이 코드를 실행해요.
            [-0.6, 0.8, 0.2],
            # 설명: 이 코드를 실행해요.
            [0.2, -0.1, 0.9],
        # 설명: 이 코드를 실행해요.
        ],
        # 설명: 'dtype' 변수에 값을 계산해서 저장해요.
        dtype=float,
    # 설명: 이 코드를 실행해요.
    )
    # 설명: 파이썬 리스트·시퀀스를 NumPy 배열로 변환해요.
    b = np.array([0.1, -0.2, 0.05], dtype=float)

    # 설명: 'logits' 변수에 값을 계산해서 저장해요.
    logits = X @ W + b
    # 설명: ReLU 활성화 함수를 적용한 결과를 저장해요.
    relu_out = np.maximum(0.0, logits)

    # 설명: '{'을(를) 함수 호출 측에 반환해요.
    return {
        # 설명: 이 코드를 실행해요.
        "chapter": "chapter67",
        # 설명: 이 코드를 실행해요.
        "topic": "인공신경망이란",
        # 설명: 이 코드를 실행해요.
        "lesson_10min": LESSON_10MIN,
        # 설명: 이 코드를 실행해요.
        "practice_30min": PRACTICE_30MIN,
        # 설명: 값을 파이썬 리스트로 변환해요.
        "input_shape": list(X.shape),
        # 설명: 값을 파이썬 리스트로 변환해요.
        "weight_shape": list(W.shape),
        # 설명: 값을 파이썬 리스트로 변환해요.
        "output_shape": list(logits.shape),
        # 설명: NumPy 배열을 파이썬 리스트로 변환해요.
        "logits": logits.round(4).tolist(),
        # 설명: NumPy 배열을 파이썬 리스트로 변환해요.
        "relu_output": relu_out.round(4).tolist(),
    # 설명: 이 코드를 실행해요.
    }


# 설명: 이 파일을 직접 실행했을 때만 아래 코드를 수행해요.
if __name__ == "__main__":
    # 설명: 값을 정수형으로 변환해요.
    print(run())
